#include<stdio.h>

int f[2] = {1, 2};

int main() {
	int sum = 2;
	for (int i = 0; f[i % 2] + f[(i + 1) % 2] < 4000000; i++) {
		f[i % 2] += f[(i + 1) % 2];
		!(f[i % 2] & 1) && (sum += f[i % 2]);
	}
	printf("%d\n", sum);

	return 0;
}
