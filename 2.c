#include<stdio.h>

int f[50] = {0};

int main() {
	int n = 2, sum = 2;
	f[0] = 2;
	f[1] = 1;
	f[2] = 2;
	while (f[f[0]] <= 4000000) {
		f[0]++;
		f[f[0]] = f[f[0] - 1] + f[f[0] - 2];
		!(f[f[0]] & 1) && (sum += f[f[0]]);
	}
	printf("%d\n", sum);

	return 0;
}
